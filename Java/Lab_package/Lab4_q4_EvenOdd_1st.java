package Lab_package;

public class Lab4_q4_EvenOdd_1st {

    
    public static void evenOdd_Check(int num1){

        int number1 = num1;

        if(number1 % 2 == 0){

            System.out.println("The Number :" + number1 + " is EVEN.");

        }else{

            System.out.println("The Number :" + number1 + " is ODD.");


        }

    }


}
